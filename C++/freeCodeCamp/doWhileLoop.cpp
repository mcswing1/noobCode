#include <iostream>
using namespace std;

int main()
{
    
    int index = 6;
    // A do-while loop is a normal while loop but it is reversed. The code is executed first and then the condition is checked. 
    do {

        cout << index << endl;
        index++; 

    } while(index <= 5);

    return 0;
}